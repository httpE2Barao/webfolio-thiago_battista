"use client";

import { useState, useEffect } from 'react';
import Image from 'next/image';

interface OptimizedImageProps {
  src: string;
  alt: string;
  width?: number;
  height?: number;
  className?: string;
  priority?: boolean;
  onLoad?: () => void;
  onError?: () => void;
  sizes?: string;
  quality?: number;
  placeholder?: 'blur' | 'empty';
  blurDataURL?: string;
}

// Cache para imagens j√° carregadas
const imageCache = new Map<string, boolean>();

export function OptimizedImage({
  src,
  alt,
  width = 800,
  height = 600,
  className = "",
  priority = false,
  onLoad,
  onError,
  sizes = "(max-width: 768px) 100vw, (max-width: 1200px) 50vw, 33vw",
  quality = 80,
  placeholder = 'empty',
  blurDataURL
}: OptimizedImageProps) {
  const [imgSrc, setImgSrc] = useState(src);
  const [isLoading, setIsLoading] = useState(true);
  const [hasError, setHasError] = useState(false);
  const [retryCount, setRetryCount] = useState(0);

  // Verificar se a imagem j√° est√° no cache
  const isCached = imageCache.has(src);

  useEffect(() => {
    // Se a imagem j√° est√° no cache, n√£o precisa mostrar o estado de carregamento
    if (isCached) {
      setIsLoading(false);
    }
  }, [isCached]);

  // Auto-reload functionality
  useEffect(() => {
    const reloadInterval = setInterval(() => {
      if (hasError && retryCount < 3) {
        const timestamp = new Date().getTime();
        setImgSrc(`${src}?t=${timestamp}`);
        setHasError(false);
        setIsLoading(true);
        setRetryCount(prev => prev + 1);
      }
    }, 5000); // Try to reload every 5 seconds

    return () => clearInterval(reloadInterval);
  }, [hasError, retryCount, src]);

  const handleLoad = () => {
    console.log('‚úÖ OptimizedImage: Loaded successfully', src);
    setIsLoading(false);
    setHasError(false);
    
    // Adicionar ao cache quando carregada com sucesso
    imageCache.set(src, true);
    
    onLoad?.();
  };

  const handleError = () => {
    console.log('‚ùå OptimizedImage: Failed to load', src);
    setIsLoading(false);
    setHasError(true);
    onError?.();
  };

  // Se o placeholder √© 'blur' mas n√£o temos blurDataURL, mudar para 'empty'
  const effectivePlaceholder = placeholder === 'blur' && !blurDataURL ? 'empty' : placeholder;

  return (
    <div className={`relative ${className}`}>
      {isLoading && !isCached && (
        <div className="absolute inset-0 bg-gray-800 animate-pulse flex items-center justify-center">
          <div className="text-gray-200">Carregando...</div>
        </div>
      )}
      
      {hasError ? (
        <div className="absolute inset-0 bg-gray-800 flex flex-col items-center justify-center">
          <div className="text-gray-200 mb-2">Erro ao carregar imagem</div>
          <div className="text-sm text-gray-300">
            Tentativa {retryCount}/3 - Aguardando...
          </div>
        </div>
      ) : (
        <Image
          src={imgSrc}
          alt={alt}
          width={width}
          height={height}
          className={`${className} transition-opacity duration-300 ${isLoading && !isCached ? 'opacity-0' : 'opacity-100'}`}
          priority={priority}
          onLoad={handleLoad}
          onError={handleError}
          sizes={sizes}
          quality={quality}
          placeholder={effectivePlaceholder}
          blurDataURL={blurDataURL}
        />
      )}
    </div>
  );
}

// Hook para pr√©-carregamento de imagens com cache
export function useImagePreloader(images: string[]) {
  const [loadedImages, setLoadedImages] = useState<Set<string>>(new Set());

  useEffect(() => {
    const preloadImages = async () => {
      console.log('üîÑ ImagePreloader: Starting preload for', images.length, 'images');
      
      // Filtrar apenas imagens que ainda n√£o est√£o no cache
      const imagesToLoad = images.filter(src => !imageCache.has(src));
      
      if (imagesToLoad.length === 0) {
        console.log('‚úÖ ImagePreloader: All images already cached');
        return;
      }
      
      const promises = imagesToLoad.map(src => {
        return new Promise<void>((resolve, reject) => {
          const img = document.createElement('img');
          img.onload = () => {
            // Adicionar ao cache quando pr√©-carregada com sucesso
            imageCache.set(src, true);
            setLoadedImages(prev => new Set(prev).add(src));
            resolve();
          };
          img.onerror = reject;
          img.src = src;
        });
      });

      try {
        await Promise.all(promises);
        console.log('‚úÖ ImagePreloader: Successfully preloaded', imagesToLoad.length, 'images');
      } catch (error) {
        console.log('‚ùå ImagePreloader: Some images failed to preload:', error);
      }
    };

    if (images.length > 0) {
      preloadImages();
    }
  }, [images]);

  return loadedImages;
}

// Fun√ß√£o para limpar o cache de imagens (√∫til para liberar mem√≥ria)
export function clearImageCache() {
  imageCache.clear();
  console.log('üóëÔ∏è Image cache cleared');
}

// Fun√ß√£o para pr√©-carregar imagens cr√≠ticas com alta prioridade
export function preloadCriticalImages(images: string[]) {
  images.forEach(src => {
    if (!imageCache.has(src)) {
      const img = document.createElement('img');
      img.onload = () => imageCache.set(src, true);
      img.src = src;
    }
  });
}